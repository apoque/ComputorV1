int		ft_get_pgcd(int a, int b)
{
	int	pgcd;
	int	tmp;

	pgcd = 1;
	tmp = 1;
	a = (a < 0) ? -a : a;
	b = (b < 0) ? -b : b;
	while (tmp <= a && tmp <= b)
	{
		if (a % tmp == 0 && b % tmp == 0)
			pgcd = tmp;
		tmp++;
	}
	return (pgcd);
}
